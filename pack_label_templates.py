def normalize_pack_label_template_key(booster_name):
    clean_value = (booster_name or "").strip().lower()

    if not clean_value:
        return PACK_LABEL_TEMPLATE_DEFAULT

    if "secret lair" in clean_value:
        return "secret_lair"

    if "chaos draft" in clean_value:
        return "chaos_draft"

    if "cube" in clean_value:
        return "cube"

    if "custom" in clean_value:
        return "custom"

    if "collector" in clean_value:
        return "collector"

    if "jumpstart" in clean_value:
        return "jumpstart"

    if "play" in clean_value:
        return "play"

    if "set" in clean_value:
        return "set"

    if "draft" in clean_value:
        return "draft"

    return PACK_LABEL_TEMPLATE_DEFAULT


def get_pack_label_template(booster_name):
    template_key = normalize_pack_label_template_key(booster_name)
    template = dict(PACK_LABEL_TEMPLATES.get(template_key) or PACK_LABEL_TEMPLATES[PACK_LABEL_TEMPLATE_DEFAULT])
    template["template_key"] = template_key
    return template